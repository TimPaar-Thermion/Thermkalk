import re

def uppdatera_rorskalar(material_dict):
    # Regex som söker efter ett valfritt tal följt av mellanslag (ex. "800 "),
    # därefter fångas ett tal (diameter) följt av "/" och ett tal (isoleringstjocklek)
    m = re.compile(r'\b(?:\d+\s+)?(\d+)/(\d+)(?!\d)')
    for key, mat in material_dict.items():
        if mat.get("material typ") in ["Rörskål", "Rörskål Diff"] and (not mat.get("diameter") or mat.get("isoleringstjocklek") in [0, "0"]):
            # Försök att hitta match i artikelnamnet
            text = mat.get("artikelnamn", "")
            match = m.search(text)
            if match:
                # Första gruppen blir diameter, andra gruppen isoleringstjocklek
                mat["diameter"] = match.group(1)
                try:
                    mat["isoleringstjocklek"] = int(match.group(2))
                except ValueError:
                    mat["isoleringstjocklek"] = match.group(2)
    return material_dict


